/**
 * Returns a complete SVG HTML string for the given icon name.
 * @param {string} name — Lucide icon name (or alias like 'success', 'close')
 * @param {number} [size=24] — width/height in px
 * @param {Record<string,string>} [attrs={}] — optional extra attributes (e.g. { style: 'animation:nativo-spin .7s linear infinite' })
 * @returns {string}
 */
export function icon(name, size = 24, attrs = {}) {
  const key = ALIASES[name] || name;
  const inner = SVG[key];
  if (!inner) return '';
  const extra = Object.entries(attrs).map(([k, v]) => ` ${k}="${v}"`).join('');
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"${extra}>${inner}</svg>`;
}

/**
 * Creates a live SVG DOM element for the given icon name.
 * @param {string} name — Lucide icon name (or alias)
 * @param {number} [size=24]
 * @returns {SVGSVGElement}
 */
export function iconElement(name, size = 24) {
  const svgEl = document.createElementNS('http://www.w3.org/2000/svg', 'svg');
  svgEl.setAttribute('width', String(size));
  svgEl.setAttribute('height', String(size));
  svgEl.setAttribute('viewBox', '0 0 24 24');
  svgEl.setAttribute('fill', 'none');
  svgEl.setAttribute('stroke', 'currentColor');
  svgEl.setAttribute('stroke-width', '2');
  svgEl.setAttribute('stroke-linecap', 'round');
  svgEl.setAttribute('stroke-linejoin', 'round');

  const key = ALIASES[name] || name;
  svgEl.innerHTML = SVG[key] || '';
  return svgEl;
}

